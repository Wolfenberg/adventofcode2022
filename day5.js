//Impl
const moveCrates = (cratesInput, movesInput) => {
    const crates = parseCrates(cratesInput)

    const moves = parseMoves(movesInput)

    moves.forEach(step => {
        for (let i = 0; i < step.move; i++) {
            const crate = crates[step.from - 1].pop()
            crates[step.to - 1].push(crate)
        }
    })

    return crates
}

const parseCrates = (input) => {
    const result = []
    const rows = input.split('\n')
    for (let i = 0; i < rows[0].length; i++) {
        result.push([])
        for (let j = rows[0].length - 1; j >= 0; j--) {
            const el = rows[j][i]
            // No empty spaces
            if (el !== ' ') {
                result[i].push(el)
            }

        }
    }
    return result
}

const parseMoves = (string) => {
    return string.split('\n').map(move => {
        const result = move.split(' ')
        return {
            move: Number(result[1]),
            from: Number(result[3]),
            to: Number(result[5])
        }
    })
}

